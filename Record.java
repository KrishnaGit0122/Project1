
public class Record {
    private String key;
    private Node value;
    
    public Record(String key, Node value)
    {
        this.key = key;
        this.value = value;
    }
    
    

    public String getKey()
    {
        return this.key;
    }
    
    public Node getValue()
    {
        return this.value;
    }

    
    

}
